package forTrash;

public class SubMenuDTO {
	
	private String coSubMenu;
	private String noSubMenu;
	private String alSubMenu;
	private String ruta;
	private String coMenuPadre;
	private String icono;
	
	public String getCoSubMenu() {
		return coSubMenu;
	}
	public void setCoSubMenu(String coSubMenu) {
		this.coSubMenu = coSubMenu;
	}
	public String getNoSubMenu() {
		return noSubMenu;
	}
	public void setNoSubMenu(String noSubMenu) {
		this.noSubMenu = noSubMenu;
	}
	public String getAlSubMenu() {
		return alSubMenu;
	}
	public void setAlSubMenu(String alSubMenu) {
		this.alSubMenu = alSubMenu;
	}
	public String getRuta() {
		return ruta;
	}
	public void setRuta(String ruta) {
		this.ruta = ruta;
	}
	public String getCoMenuPadre() {
		return coMenuPadre;
	}
	public void setCoMenuPadre(String coMenuPadre) {
		this.coMenuPadre = coMenuPadre;
	}
	public String getIcono() {
		return icono;
	}
	public void setIcono(String icono) {
		this.icono = icono;
	}
	
	

}
