package com.wollcorp.beans.forecast;

public class Resultado {
	
	private String coFcst;
	private String coLinea;
	private int nrOrde;
	private String noLinea;
	private String pod;
	private String size;
	private String tipo;
	private String estado;
	private int cantidad;
	private int pesoKg;
	
	public String getCoFcst() {
		return coFcst;
	}
	public void setCoFcst(String coFcst) {
		this.coFcst = coFcst;
	}
	public String getCoLinea() {
		return coLinea;
	}
	public void setCoLinea(String coLinea) {
		this.coLinea = coLinea;
	}
	public String getNoLinea() {
		return noLinea;
	}
	public void setNoLinea(String noLinea) {
		this.noLinea = noLinea;
	}
	public String getPod() {
		return pod;
	}
	public void setPod(String pod) {
		this.pod = pod;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public int getCantidad() {
		return cantidad;
	}
	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}
	public int getPesoKg() {
		return pesoKg;
	}
	public void setPesoKg(int pesoKg) {
		this.pesoKg = pesoKg;
	}
	public int getNrOrde() {
		return nrOrde;
	}
	public void setNrOrde(int nrOrde) {
		this.nrOrde = nrOrde;
	}

}
