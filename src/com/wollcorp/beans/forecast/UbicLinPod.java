package com.wollcorp.beans.forecast;

public class UbicLinPod {
	
	private String coLine;
	private String noLine;
	private String coPuer;
	private String coPuerSol;
	private int row;
	
	public String getCoLine() {
		return coLine;
	}
	public void setCoLine(String coLine) {
		this.coLine = coLine;
	}
	public String getNoLine() {
		return noLine;
	}
	public void setNoLine(String noLine) {
		this.noLine = noLine;
	}
	public String getCoPuer() {
		return coPuer;
	}
	public void setCoPuer(String coPuer) {
		this.coPuer = coPuer;
	}
	public String getCoPuerSol() {
		return coPuerSol;
	}
	public void setCoPuerSol(String coPuerSol) {
		this.coPuerSol = coPuerSol;
	}
	public int getRow() {
		return row;
	}
	public void setRow(int row) {
		this.row = row;
	}

}
