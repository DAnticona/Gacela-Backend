package com.wollcorp.dto;

import java.util.Date;

public class LineaDTO {
	
	private String coLine;
	private String noLine;
	private String coIso;
	private String coSol;
	private String fgProp;
	private String usCrea;
	private String usModi;
	private Date feCrea;
	private Date feModi;
	private String fgActi;
	private int nrOrde;
	
	public String getCoLine() {
		return coLine;
	}
	public void setCoLine(String coLine) {
		this.coLine = coLine;
	}
	public String getNoLine() {
		return noLine;
	}
	public void setNoLine(String noLine) {
		this.noLine = noLine;
	}
	public String getCoIso() {
		return coIso;
	}
	public void setCoIso(String coIso) {
		this.coIso = coIso;
	}
	public String getCoSol() {
		return coSol;
	}
	public void setCoSol(String coSol) {
		this.coSol = coSol;
	}
	public String getFgProp() {
		return fgProp;
	}
	public void setFgProp(String fgProp) {
		this.fgProp = fgProp;
	}
	public String getUsCrea() {
		return usCrea;
	}
	public void setUsCrea(String usCrea) {
		this.usCrea = usCrea;
	}
	public String getUsModi() {
		return usModi;
	}
	public void setUsModi(String usModi) {
		this.usModi = usModi;
	}
	public Date getFeCrea() {
		return feCrea;
	}
	public void setFeCrea(Date feCrea) {
		this.feCrea = feCrea;
	}
	public Date getFeModi() {
		return feModi;
	}
	public void setFeModi(Date feModi) {
		this.feModi = feModi;
	}
	public String getFgActi() {
		return fgActi;
	}
	public void setFgActi(String fgActi) {
		this.fgActi = fgActi;
	}
	public int getNrOrde() {
		return nrOrde;
	}
	public void setNrOrde(int nrOrde) {
		this.nrOrde = nrOrde;
	}

}
